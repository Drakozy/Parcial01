    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            double b, e, r;
            b = Double.parseDouble(BaseText.getText());
            e = Double.parseDouble(ExpoText.getText());
            r = Math.pow(b, e);
            ResuText.setText(String.valueOf(r));
        }
        catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un número entero válido", "Error", JOptionPane.ERROR_MESSAGE);
        }              
    }//GEN-LAST:event_jButton2ActionPerformed

